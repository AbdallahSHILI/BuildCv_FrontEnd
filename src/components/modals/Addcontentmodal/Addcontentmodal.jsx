const AddContentModal = ({ isOpen, onClose, onAdd, addedSections = [] }) => {
  if (!isOpen) return null;

  const handleBackdropClick = (e) => {
    if (e.target === e.currentTarget) onClose();
  };

  return (
    <div className={styles.modalBackdrop} onClick={handleBackdropClick}>
      <div className={styles.addContentModal}>
        {/* Header */}
        <div className={styles.addContentModalHeader}>
          <div className={styles.addContentModalTitle}>
            <h2 className={styles.addContentTitle}>Add content</h2>
            <div className={styles.quickStart}>
              <span className={styles.quickStartLabel}>Quick start:</span>
              <button className={styles.importBtn}>
                <svg
                  width="15"
                  height="15"
                  viewBox="0 0 24 24"
                  fill="none"
                  stroke="currentColor"
                  strokeWidth="2"
                >
                  <polyline points="8 17 12 21 16 17" />
                  <line x1="12" y1="12" x2="12" y2="21" />
                  <path d="M20.88 18.09A5 5 0 0 0 18 9h-1.26A8 8 0 1 0 3 16.29" />
                </svg>
                Import Resume
              </button>
            </div>
          </div>
          <button className={styles.modalCloseBtn} onClick={onClose}>
            <svg
              width="18"
              height="18"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2.5"
            >
              <line x1="18" y1="6" x2="6" y2="18" />
              <line x1="6" y1="6" x2="18" y2="18" />
            </svg>
          </button>
        </div>

        {/* Grid */}
        <div className={styles.addContentGrid}>
          {SECTIONS.map((section) => {
            const isAdded = addedSections.includes(section.key);
            return (
              <button
                key={section.key}
                className={`${styles.addContentCard} ${section.dashed ? styles.addContentCardDashed : ""} ${isAdded ? styles.addContentCardAdded : ""}`}
                onClick={() => !isAdded && onAdd(section.key)}
                disabled={isAdded}
              >
                <div className={styles.addContentCardIcon}>{section.icon}</div>
                <div className={styles.addContentCardText}>
                  <p className={styles.addContentCardLabel}>{section.label}</p>
                  <p className={styles.addContentCardDesc}>
                    {section.description}
                  </p>
                </div>
                {isAdded && (
                  <div className={styles.addContentCardCheck}>
                    <svg
                      width="12"
                      height="12"
                      viewBox="0 0 24 24"
                      fill="none"
                      stroke="currentColor"
                      strokeWidth="3"
                    >
                      <polyline points="20 6 9 17 4 12" />
                    </svg>
                  </div>
                )}
              </button>
            );
          })}
        </div>
      </div>
    </div>
  );
};

